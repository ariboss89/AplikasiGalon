    private void txtTotalItemKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTotalItemKeyTyped
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();

        if (!(((karakter >= '0') && (karakter <= '9') || (karakter == KeyEvent.VK_BACK_SPACE) || (karakter == KeyEvent.VK_DELETE) || (karakter == KeyEvent.VK_ENTER)))) {
            getToolkit().beep();
            evt.consume();
        }

        if (txtTotalItem.getText().length() == 0 && karakter == KeyEvent.VK_SPACE) {
            getToolkit().beep();
            evt.consume();
        }

        if (txtTotalItem.getText().length() >= 12) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtTotalItemKeyTyped

    private void cbPelanganItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbPelanganItemStateChanged
        // TODO add your handling code here:
        con = new Koneksi();
        try {
            st = con.connect().createStatement();
            rs = st.executeQuery("SELECT *FROM tb_pelanggan WHERE nama = '" + cbPelangan.getSelectedItem().toString() + "'");
            while (rs.next()) {
                txtKontak.setText(rs.getString("kontak"));
                txtAlamat.setText(rs.getString("alamat"));
                txtQty.requestFocus();
            }
        } catch (SQLException ex) {

        }
    }//GEN-LAST:event_cbPelanganItemStateChanged

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (jDateChooser2.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Silahkan Isi Tanggal !!");
            jDateChooser2.requestFocus();
        } else if (cbNamaKaryawan.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Karyawan !!");
            cbNamaKaryawan.requestFocus();
        } else if (cbPelangan.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Pelanggan !!");
            cbPelangan.requestFocus();
        } else if (Integer.parseInt(txtQty.getText()) == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Isi Qty !!");
            txtQty.requestFocus();
        } else {

            DefaultTableModel dataModel = (DefaultTableModel) jTable1.getModel();
            ArrayList list = new ArrayList<>();
            jTable1.setAutoCreateColumnsFromModel(true);
            list.add(txtNamaItem.getText());
            list.add(cbPelangan.getSelectedItem().toString());
            list.add(txtAlamat.getText());
            list.add(txtQty.getText());
            list.add(txtHarga.getText());
            list.add(txtTotal.getText());
            dataModel.addRow(list.toArray());

            int totalItem = 0;
            int total = 0;
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                totalItem = totalItem + (Integer.parseInt(jTable1.getValueAt(i, 3).toString()));
                txtTotalItem.setText(Integer.toString(totalItem));

                total = total + (Integer.parseInt(jTable1.getValueAt(i, 5).toString()));
                tbp.setTotal(total);
            }

            txtQty.setText("0");
            txtTotal.setText("0");
            cbPelangan.setSelectedIndex(0);
            txtKontak.setText("");
            txtAlamat.setText("");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

