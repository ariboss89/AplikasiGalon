    private void IdPemasukan() {
        con = new Koneksi();
        try {
            st = con.connect().createStatement();
            rs = st.executeQuery("SELECT *FROM tb_penjualan ORDER BY id_penjualan DESC");
            if (rs.first() == false) {
                tbp.setId_penjualan("P001");
            } else {
                rs.first();
                System.out.println("COT: " + rs.getString("id_penjualan").substring(1, 4));
                int nokirim = Integer.valueOf(rs.getString("id_penjualan").substring(1, 4)) + 1;
                System.out.println(nokirim);
                if (nokirim < 10) {
                    tbp.setId_penjualan("P00" + nokirim);
                } else if (nokirim >= 10 && nokirim < 100) {
                    tbp.setId_penjualan("P0" + nokirim);
                } else if (nokirim >= 100 && nokirim < 1000) {
                    tbp.setId_penjualan("P" + nokirim);
                }
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

