    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyChar() == KeyEvent.VK_DELETE) {
            int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (ok == 0) {
                DefaultTableModel dataModel = (DefaultTableModel) jTable1.getModel();
                int select = jTable1.getSelectedRow();
                dataModel.removeRow(select);

                int a = jTable1.getRowCount();
                int totalItem = 0;
                int total = 0;

                if (a == 0) {
                    txtTotalItem.setText("0");
                } else {
                    for (int i = 0; i < a; i++) {
                        totalItem = totalItem + (Integer.parseInt(jTable1.getValueAt(i, 3).toString()));
                        txtTotalItem.setText(Integer.toString(totalItem));

                        total = total + (Integer.parseInt(jTable1.getValueAt(i, 5).toString()));
                        tbp.setTotal(total);
                    }
                }
            } else {

            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
        int rowCount = jTable1.getRowCount();
        String idmasuk = tbp.getId_penjualan();

        if (rowCount == 0) {
            JOptionPane.showMessageDialog(null, "Tabel Masih Kosong !!");
        } else {
            SimpleDateFormat spf = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggal = java.sql.Date.valueOf(spf.format(jDateChooser2.getDate()));
            int jumlahItem = Integer.parseInt(txtTotalItem.getText().trim());
            int totalSemua = (tbp.getTotal());
            String namakaryawan = cbNamaKaryawan.getSelectedItem().toString();
            for (int i = 0; i < rowCount; i++) {
                String nama = (String) jTable1.getValueAt(i, 0);
                String pelanggan = (String) jTable1.getValueAt(i, 1);
                String alamat = (String) jTable1.getValueAt(i, 2);
                int jumlah = (Integer.parseInt(jTable1.getValueAt(i, 3).toString()));
                int harga = (Integer.parseInt(jTable1.getValueAt(i, 4).toString()));
                int total = (Integer.parseInt(jTable1.getValueAt(i, 5).toString()));

                sld.SaveDetail(idmasuk, nama, pelanggan, alamat, jumlah, harga, total);
            }

            sld.Save(idmasuk, tanggal, jumlahItem, totalSemua, namakaryawan);
        }

        //sld.CetakStruk(idmasuk, namaSupplier);
        Refresh();
//        cbNamaKaryawan.removeAllItems();
//        cbNamaKaryawan.addItem("PILIH");
//        cbPelangan.removeAllItems();
//        cbPelangan.addItem("");
    }//GEN-LAST:event_btnSelesaiActionPerformed

